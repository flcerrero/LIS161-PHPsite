	<p id="history"><b>History</b></p>
	<img id="featuredpic" src="img/church.jpg">
		<p>The historic town of Tanza where Gen. Emilio Aguinaldo took his oath as first President of the Republic at the convent of the Holy Cross Church lies at the northwest part of Cavite. Tanza occupies 7824.29 hectares representing 5.48% of the total land area of Cavite Province. The town is situated 33 kilometers south away from Manila and 13 kilometers away from Trece Martirez City.
<br><br>
The first class municipality is now experiencing rapid urban growth. Such  phenomenon is anticipated since much of its area covering 4758.20 hectares is characterized by flatlands, which is low susceptible to erosion and is recommended for high density urban development. Whereas the 2069.20 hectares gently sloping areas are appropriate for medium density urban development. Rapid urbanization indeed could be attributed to the housing projects and availability of low cost housing besides the existence of several industrial establishments and its proximity to Cavite Export Processing Zone. The operation of commercial establishments within the Poblacion and the escalating activities of commerce and trade at Daang Amaya I where Tanza Public Market and popular fast-food chains are perceptible is likewise a contributing factor.
<br><br>
Farming anf fishing industry is considered to be the major source of economy. Farming is attributed to the 1896.71 has. Of agricultural lot 
wherein 1822.71 has. are planted with rice and vegetables and the remaining 14 hectares are devoted to livestock, poultry production and aquaculture. With the presence of five(5) major fishing grounds along coastal barangays, fishing is similarly is a chief means of living and is generally a year round activity usually taken as a family enterprise. Beaches and resorts within this area cater to the tourism industry for the populace.
<br><br>
The key concern of economic sector is to boost agriculture and fisheries production, enhance tourism industry, increase employment and profit opportunities as well as upgrade human and physical resources. The social sector aims for quality health and nutrition services,  quality education and improved housing and basic utilities. The infrastructure sector plans for an expanded infrastructure support for production.
<br><br>
The pursuit of these concerns relies on the hand of the local chief executive and the legislative. Their good and harmonious relationship will boost the morale and enhance effectiveness of local institutions as well as strengthen local capabilities thereby the welfare of the people will not be at stake. With joined hands and unified actions there will be no cause of impediment and the realization of identified plans and programs paramount for the advancement of Tanza community will indisputably be procured.</p>
